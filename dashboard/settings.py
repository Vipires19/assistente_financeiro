# Database
# Configuração MongoDB (não usa ORM relacional do Django)
# O acesso ao MongoDB é feito diretamente via pymongo nos repositories

# Configuração do MongoDB
# Substitua pela sua string de conexão do MongoDB
MONGO_USER = urllib.parse.quote_plus(os.getenv("MONGO_USER"))
MONGO_PASS = urllib.parse.quote_plus(os.getenv("MONGO_PASS"))
MONGO_HOST = os.getenv("MONGO_HOST")
MONGO_DB_NAME = os.getenv("MONGO_DB_NAME", "financeiro_db")

MONGO_URI = (
    f"mongodb+srv://{MONGO_USER}:{MONGO_PASS}@{MONGO_HOST}/{MONGO_DB_NAME}?retryWrites=true&w=majority"
)

MONGODB_SETTINGS = {
    "URI": MONGO_URI,
    "DB_NAME": MONGO_DB_NAME,
}

mongoengine.connect(
    db=MONGO_DB_NAME,
    host=MONGO_URI,
    alias="default"
)


